class SPP(nn.Module):
    # Spatial pyramid pooling layer used in YOLOv3-SPP
    def __init__(self, c1, c2, k=(5, 9, 13)):
        super(SPP, self).__init__()
        c_ = c1 // 2  # hidden channels
        self.cv1 = Conv(c1, c_, 1, 1)
        self.cv2 = Conv(c_ * (len(k) + 1), c2, 1, 1)
        self.m = nn.ModuleList([nn.MaxPool2d(kernel_size=x, stride=1, padding=x // 2) for x in k])

    def forward(self, x):
        x = self.cv1(x)
        return self.cv2(torch.cat([x] + [m(x) for m in self.m], 1))


class MSA(nn.Module):
    # Spatial pyramid pooling layer used in YOLOv3-SPP
    def __init__(self, c1, c2, k=(5, 9, 13)):
        super(MSA, self).__init__()
        c_ = c1 // 2  # hidden channels
        self.cv1 = Conv(c1, c_, 1, 1)
        self.cv2 = Conv(c_ * (len(k) + 1), c2, 1, 1)
        self.m = nn.ModuleList([nn.MaxPool2d(kernel_size=x, stride=1, padding=x // 2) for x in k])
        self.sSE = sSE(c1, 1)

    def forward(self, x):
        x = self.cv1(x)
        x = self.cv2(torch.cat([x] + [m(x) for m in self.m], 1))
        x = self.sSE(x)
        return x


class MSA1(nn.Module):
    # Spatial pyramid pooling layer used in YOLOv3-SPP
    def __init__(self, c1, c2):
        super(MSA1, self).__init__()
        c_ = c1 // 2  # hidden channels
        self.cv1 = Conv(c1, c_, 1, 1)
        self.cv2 = Conv(c_, c2, 1, 1)
        self.m1 = nn.MaxPool2d(kernel_size=3, stride=1, padding=1)
        self.m2 = nn.MaxPool2d(kernel_size=5, stride=1, padding=2)
        self.m3 = nn.MaxPool2d(kernel_size=7, stride=1, padding=3)
        # self.cv2 = Conv(c_ * (len(k) + 1), c2, 1, 1)
        # self.m = nn.ModuleList([nn.MaxPool2d(kernel_size=x, stride=1, padding=x // 2) for x in k])
        self.sSE = sSE(c_, 1)

    def forward(self, U):
        x = self.cv1(U)
        y = self.sSE(x)
        y1 = self.sSE(self.m1(x))
        y2 = self.sSE(self.m2(x))
        y3 = self.sSE(self.m3(x))
        out = self.cv2(y + y1 + y2 + y3)
        return out

